    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        try {
        String nome = txtNome.getText();
        String email = txtEmail.getText();
        String telefone = txtTelefone.getText();
        String senha = "";
        String senhaConfirm = "";
        char[] senhaView = txtSenha.getPassword();
        char[] ConfirmSenha = txtConfirmarSenha.getPassword();
        
        for(char c : senhaView) {
          senha += c;  
        }
         
          for(char c : ConfirmSenha) {
          senhaConfirm += c;  
        }
        
        if(senha.length() < 8) {
            JOptionPane.showMessageDialog(null, "A senha deve ter pelo menos 8 digitos");
            return;
        }
               
        if(!senha.equals(senhaConfirm)) {
            JOptionPane.showMessageDialog(null, "As senhas não são iguais");
            return;
        }
        
        boolean mail = email.matches(".*[@][a-zA-Z]*[.][a-z]{3}");
        
        if(mail == false) {
            JOptionPane.showMessageDialog(null, "Insira um email válido");
            return;
        }
        
        boolean tel = telefone.matches("[0-9]{11}");
        
        if(tel == false) {
           JOptionPane.showMessageDialog(null, "Insira um número de telefone válido (numero com 11 digitos)");
            return;
        }
        
        
        
        if(nome.isEmpty() || telefone.isEmpty() || email.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos");
            return;
        }
        Usuario user = new Usuario();
        
        user.setNome(nome);
        user.setEmail(email);      //Salva as informações de cadastro do usuario 
        user.setTelefone(telefone);
        user.setSenhaHash(Bcrypt.gerarHash(senha));
        user.setSalt(Bcrypt.gerarSalt());
        
        usuarioAtual.setUser(user); //Salva as informações em UsuarioAtual
        
        telaCadastroSenhaMestra tcm = new telaCadastroSenhaMestra();
        tcm.setVisible(true);
        dispose();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao fazer cadastro");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

