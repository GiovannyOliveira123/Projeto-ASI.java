    private void brnVerSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnVerSenhaActionPerformed
       
    if(senhaVisivel) {
        txtSenha.setEchoChar('*'); //Se a senha for visivel e usuario apertar o botão, esconde a senha
        senhaVisivel = false; //Deixa a variavel de controle como senha não visivel
        txtSenha.requestFocus(); //Retorna para o campo senha
    }
    
    else {
        txtSenha.setEchoChar((char)0); //Torna a senha visivel
        senhaVisivel = true;
        txtSenha.requestFocus();
    }
    
        
        
        
    }//GEN-LAST:event_brnVerSenhaActionPerformed

